<h1> Pourquoi la barbe est à la mode? </h1>
<div class='p3' >

Au travers des époques, la barbe a eu des significations différentes. Un jour symbole de la masculinité, l’autre un signe religieux, un acte politique ou encore un simple artifice de mode. La barbe se met dans tout ses états révélant des évolution de mentalités.
Qui ne connait pas la barbe de Pierre Curie? Elle un des signes distinctif de ce grand physicien français.
Selon les époques, il est impossible d’ignorer que la barbe a pu prendre des significations tout a fait différentes :
Dans l’histoire, elle a tout d’abord été abordée par des dieux, prophètes, mais aussi des philosophes, des médecins ou même des empereurs. Elle a été un symbole de sagesse et de savoir dans histoire étant arboré par de grands personnages.
Selon l’âge, elle peut être un symbole de virilité et de masculinité, donner de l’autorité..
Aujourd’hui, la barbe est très à la mode et est arborée par un grand nombre d’hommes (92% d’hommes l’arborent, c’est énorme !), et a même son propre courant de mode : les hipster.
Ce terme est apparu au États Unis en 2003. Il se définit comme jeune urbain avec un style vestimentaire à contre-courant résidant principalement dans les quartiers gentrifié. Ce mouvement est devenu un véritable business, un véritable mouvement de mode avec ces propres groupes et rassemblements.
La barbe est aussi un prétexte de bonne action, notamment avec le mouvement movember.
Pendant le mois de novembre tous les hommes qui y participe laisse pousser la moustache, pour sensibiliser l’opinion publique à diverses maladies masculines, comme le cancer de la prostate notamment.
Cette action est menée depuis 2003 par une association australienne.
La Barbe, c’est aussi un groupe d’action féministe, dénonçant le “ monopole du pouvoir, prestige et de l’argent par quelques milliers d’hommes blancs”. Elles souhaitent intégrer les femmes dans des “milieux d’hommes crées pour des hommes”.
Leur action principal est de se rendre dans des lieux ou les hommes sont majoritaires avec des barbes, pour enfin obtenir une visibilité : les scènes sont toujours silencieuses et les pièces parlent d’elles-mêmes : leurs présences est dû à la barbe, symbole ici de masculinité.
La barbe a une signification bien plus forte qu’on pourrait le penser. A travers la monde, elle prend une importance différente pour les populations qui l’arborent.

