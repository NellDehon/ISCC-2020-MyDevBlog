<h1> 
S’échapper sans échappements ? </h1>
<div class='p2' >


Actuellement en pleine prise de conscience écologique, voyager est devenu une question épineuse. Prendre l’avion ou protéger la planète ? Décision difficile lorsqu’on voyage généralement pour la découvrir. Comment faire ?

Pourquoi la question se pose t elle ? 
Récemment , le kérosène est au centre des débats sur la pollution.
L’avion est l’un des modes de transports préféré d’un bon nombre de pays, mais aussi un des plus polluants. Le tourisme de masse a des conséquences sur nos paysages, nos villes s’affaissent, nos forêts manquent d’oxygène et bien d’autres.
Il est devenu nécessaire de limiter l’impact écologique lors des déplacements, de penser autrement même si rien n’est simple, et que la conduite du changement peut s’avérer être longue .
Que faire ?
Si l’on commençait par des choses simples, pour s’habituer, pour penser autrement notre planète , sans pour autant se forcer ou révolutionner nos voyages de façons brutales, ce qui de toute façon, nous découragerait plus qu’autre chose.
Favoriser l’économie locale est l’une des manières plutôt agréable d’apporter sa contribution.

Si certaines astuces peuvent paraître évidentes, certaines sont bien plus méconnues et ont pourtant un impact tout autant positif :

Découvrir l’implication d’un village sur des propositions d’activités locales (peut-être qu’une ballade en pirogue vous fera vivre une expérience inoubliable). Forcément, un tour en jet ski , c’est sympa, mais finalement la pirogue, ce sera peut être plus drôle et original à raconter à vos amis.. 
Oubliez les balades à dos d’éléphants, les photos avec les tigres qui sont drogués pour être tendres et doux, et tous les autres pièges touristiques qui en réalité nuisent à ses espèces sauvages qu’il ne faut pas déranger.
Manger local, partez à la découverte de nouvelles saveurs, oubliez les fast food, chiner des petits établissements à la bonne table familiale.
Vous visitez les lieux ? Le vélo, la marche, rien de plus agréable pour s’imprégner d’un endroit.
N’hésitez pas à ne pas géo localiser vos clichés pour ne pas provoquer et empirer le tourisme de masse lorsque vous partagez vos souvenirs via les réseaux sociaux.
Il existe aussi des agences de voyages responsables comme GLOBETHIK, qui vous propose des voyages et des activités responsables, pour vous simplifiez la tâche.
Sur certains sites Internet, on vous propose également de faire des dons pour rembourser votre empreinte écologique. Cet argent est utilisé pour financer ,par exemple, la replantation d’arbres !

Mais revenons à notre “engin polluant “ : l’avion ! 
Ne négligeons pas l’implication des hommes qui ont, au péril de leur vie, développer ce moyen de transport . 
Il apporte une liberté au travers le monde que nous ne pouvons pas snober. 
Partir au bout du monde, pour sa lune de miel, réaliser un rêve de voyageur … en pirogue ça peut s’avérer compliqué ! Il se peut que vous soyez dans la nécessité de l’emprunter, dans ces cas là privilégiez les longs voyages ,de plusieurs semaines ou même plusieurs mois. 
Malgré tout , chacun peut engager une contribution personnelle sans tomber dans l’extrême : vous partez en weekend en Europe avec vos amis , pensez train !
Air France s’engage , mais nous ne sommes pas encore à l’ère de l’huile de pépin de raisin .. si vous voulez encore aller plus loin , privilégiez les pays qui avancent aux pays qui reculent.

Nous nous devons de protéger notre terre comme elle nous protège depuis la nuit des temps..
La première chose, à la portée de chacun, c’est d’ouvrir les yeux et de ne pas oublier que si tant de belles choses sont possible, c’est grâce à notre belle planète. Si nous voulons encore faire de si beaux voyages, il est essentiel de préservez notre maison, la Terre.
