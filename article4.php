<h1> Voyage aux Pays-Bas </h1>
<div class='p5' >

<B>Depuis ma première visite dans ce pays lors d’un voyage scolaire en 2016 avec mon lycée, il m’est paru inconcevable de ne plus y remettre les pieds. Je vous emmène aujourd’hui à la découverte des Pays-Bas, un grand coup de coeur. </B>

Lors de mon premier voyage, j’ai eu la chance de me rendre à Bergen Op Zoom et à Rotterdam, et par la suite, j’ai également eu la chance de découvrir Amsterdam, mais aussi Utrecht. 

Revenons à Bergen Op Zoom, une ville très peu touristique (pour ne pas dire : pas touristique), très calme avec de très beaux coins natures, idéal pour se ressourcer. Le Duinkerkenpark est sublime ! 
Ensuite, Rotterdam est personnellement mon plus grand coup de coeur. Cette ville dégage de bonnes ondes, dès que j’y arrive je me sens mieux. Ville presque entièrement détruite pendant la seconde guerre mondiale, elle a été presque complètement reconstruite et elle se démarque totalement du style d’autres grandes villes du pays comme Amsterdam dont je parlerais plus tard. Au niveau de l’architecture, la ville est plus proche de Chicago que d’Amsterdam. 
Elle regorge de musées, de balades, de commerces typiques, de restaurants.. Et de verdure ! 
La ville possède aussi quelques sublimes parcs, qui permettent un grand ressourcement, comme le HET PARK par exemple. 
Tout proche, nous avons la EUROMAST, la tour avec vue a 360° sur la ville, autant vous dire : une vue magnifique ! On peut même y faire de la descente en rappel ou de la tyrolienne, incroyable..
Et surtout : le MARKTHAL, le fameux marché de Rotterdam, avec son plafond peint et coloré et ses grandes vitres laissant entrer le soleil. Au coeur de la ville, il la fait vivre, avec ces nombreuses rues commerçantes et vivantes aux alentours, et la gare qui nous y emmène à mois de 500 mètres, produits locaux à gogo, un vrai plaisir pour les papilles ! 
C’est aussi une ville qui vit par le street-art, les murs en sont souvent remplis et cela donne un style très particulier à la ville.
La ville est très peu touristique, et c’est très appréciable de pouvoir marcher tranquillement dans la rue, au beau milieu des locaux. 
Amsterdam, ville libre, remplie de musées, de canaux, de touristes.. 
La ville est très belle et possède de nombreux incontournables : Le musée d’Anne Frank,  le musée Van Gogh, le musée du sexe et encore bien d’autres, la ville en possède de nombreux ! Il y a aussi le quartier rouge, la vieille église d’Amsterdam, les coffees-shops, .. Vous pouvez aussi vous balader et découvrir la ville en bateau ! 
Même si le mieux selon moi reste de se balader à pied ou à vélo dans la ville, pour mieux la découvrir..
Malheureusement, la ville est très très touristique ( à tel point que celui-ci va devoir être réglementée et encadré). Faites attention d’ailleurs aux fameux vélos (qui sont prioritaires par ailleurs), parfois très nombreux en période touristique, avec le flux de monde, cela peut vite devenir dangereux 
Utrecht est la dernière destination dans laquelle je me suis rendu. C’est un mélange entre Rotterdam et Amsterdam, l’architecture typique des pays-bas comme à Amsterdam avec le calme de Rotterdam, voir même plus ! 
La ville étant beaucoup plus petite que les deux précédentes, il est beaucoup plus simple de la découvrir, si la marche ne vous dérange pas, on peut presque tout faire en une journée! 
N’hésitez pas a vous rendre dans un petit café placé sur une péniche le long d’un canal afin de vous relaxer et y prendre un café dans un endroit typique. 
Vous pourrez ici y retrouvez la plus haute et la plus ancienne église des Pays-Bas, un bon point pour l’histoire ! Il y a aussi le plus grand château le Kasteel de Haar construit en 1391 et rénové en 1892, avec son très beau parc ou encore le Centraal Museum, qui est un musée d’art. 
