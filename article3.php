<h1> Les avantages de le section européenne </h1>
<div class='p2' >
<B>De plus en plus de lycées et collèges proposent une section européenne, ou orientale, dans plusieurs langues. Mais l’une des questions qui se pose est de savoir si ces classes possède un réel intérêt pour les élèves qui en font parties... </B>
En dehors de rajouter des heures de langues à la semaine, quelle est le vrai bénéfice de cette classe, même si on est plutôt moyen auparavant par exemple ?
Crées en 1992, l'éducation nationale française à mis un places les classes européennes ou orientales, avec pour motif : un besoin d’ouverture sur l’Europe et le monde. On y retrouve des langues tels que l’anglais, l’arabe, le chinois, le portugais, le vietnamien,.. et d’autres encore !
Si vous faites le choix d’intégrer une de ces classes, quelques heures d’anglais vont alors venir s’ajouter à votre emploi du temps. Egalement, une autre matière tel que l’histoire-géographie, les mathématiques, les sciences physiques ou encore l’EPS !
Le gain final ? Une mention européenne ou orientale en plus du baccalauréat, un avantage non négligeable.
Bien évidemment, tout cela représente une charge de travail supplémentaire, mais si la motivation pour apprendre les langues est présente, tout est possible. Cependant, une aptitude de base pour les langues constitue une longueur d’avance.
Durant ces cours, en plus d’un apprentissage approfondi de la langue, on en apprend également beaucoup sur l’histoire et la culture américaine et anglaise, un plus pour la culture générale !
Mais personnellement, qu’est ce que cette classe m’apporté ?
J’ai fais partie d’une classe européenne en seconde, et première en anglais. J’ai arrêté avant d’entrer en terminale et n’ai donc pas eu de mention européenne. Cependant, cela m’a tout de même beaucoup apporté.
Premièrement, le fait d’avoir plus d’heures d’anglais, nous force inconsciemment à enregistrer l’accent. L’oral est très développé dans ces classes, on est parfois entourée de plusieurs déjà bilingue ou en phase de le devenir, un élément motivant à mon humble avis.
Concernant l’oral, j’y ai personnellement réalisé de très nombreux exposés, et même une interprétation théâtralisée du livre « Of mice and men ».
Pour nous pousser à progresser en compréhension écrite, nous avons au cours de l’année, quelques livres à livre, suivi d’un compte rendu oral et écrit. Nous étions ainsi pousser un maximum vers la réussite. Mes notes ont été presque immédiatement en hausse, et l’aisance avec les langues est renforcée.

La prise à l’oral devient de plus en plus facile, comme la langue choisie de manière générale. J’ai eu personnellement la chance d’avoir une professeure géniale, très à l’écoute et pédagogue à souhait. Elle m’a énormément soutenu et aider lors de mon souhait de repasser en classe « normale », lorsque j’ai quitté la section européenne.

