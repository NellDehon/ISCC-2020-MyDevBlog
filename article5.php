Chers lecteurs,
Une très grande nouvelle, de la plus haute importance, nous est parvenu ce matin : La dernière patiente française du Covid-19 est sortie de l’hôpital, et est guérie.
Après de longs mois de guerre, de lutte contre cet ennemi invisible, sans relâche, il semble que l’humanité voit le fond du tunnel.
Au total, nous aurons perdus plus d’un million de personnes à travers le monde, cela aurait pu- être pire si la situation n’avait pas été géré correctement dans les pays d’Afrique plus pauvres, tels que le Ghana, la Somalie, etc..

Au niveau Européen; nos amis italiens ont été très affectés par ce virus. Presque les premiers Européens touchés, ils n’ont pas été les premiers à réussir à s’en défaire. Le pays à accuser 40.000, un chiffre énorme.
Pour autant, ceux-ci semble avoir appris ; le tourisme est réguler drastiquement, des mesures qu’ils traînait à prendre auparavant, les habitants eux-mêmes sembles plus respectez leurs environnements, et entourages, à se respecter entre Italiens du nord et du sud..
Une telle épreuve traversé ensemble, à semblé unir le pays, un pays qui est tombé dans une telle détresse que seuls le courage de tout à peuple à pu le remettre sur pied.
Le système hospitalier semble avoir évolué, ou au moins être désormais une priorité, aider les personnes qui y travaillent, fournir plus de moyens..
Désormais, les rares cas dans le pays sont isolés, comme dans le reste du monde.
D’une manière plus générale en Europe, une nouvelle tendance s’observe : la production dans le pays à augmenter, l’importation et l’exportation ont diminués.
L'incapacité de faire des échanges à grandes échelles pendant des mois et des mois à cause du confinement, de l’arrêt des usines etc, à permis aux pays de se rendre compte que finalement, le local n’était pas si mal, et pas si coûteux.
Les chaines de grandes distributions ont commencés à acheter plus aux producteurs locaux, puis le reste a suivi.
Évidemment, la mondialisation n’a pas disparu, il existe encore des des entreprises disséminés dans tout les pays. De fréquents échanges ont encore lieu, un pays ne peut pas nécessairement produire tout ce dont il a besoin sur ces terres.
Toujours avec une vision sur le monde, celui-ci a pris peur, chacun voyait les siens tomber malade, le monde à été effrayé.
Plus que jamais, les mesures sanitaires sont aujourd’hui partie intégrante des règles sociales ; ne vous lavez pas les mains en sortant des toilettes, et je vous assure qu’on vous dévisagera tant que vous n’oublierez plus jamais.
En France, les gens sont tellement contents de la réouverture des commerces que ce sont sont restés bondés de monde toute la journée, après une longue période de fermeture préventive. Il faut dire que le pays à fini par prendre des mesures très drastiques ; touts les commerces étaient fermées à l’exception des lieux ou un rationnement de la population n’était pas possible : Ailleurs, un couvre feu était mis en place, et les rues se sont vidées.
Les français sont passés d’une désinvolture vis-à-vis du confinement à une respect monstre, suite à une augmention massive des décès.
Le gain ? Nous avons aujourd'hui la dernière personne contaminée et hospitalisée qui sort. De nombreux tests on étés mit en place, sur presque toute la population ( à l’heure où nous
parlons, plus de 60% de la population à été testé, permettant de briser les dernières chaînes de contamination). Ainsi, il faudra attendre plusieurs semaines pour proclamer la sortie due cette pandémie, mais la France et le monde en général semble enfin sortie d’affaire.
